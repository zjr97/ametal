/**
 * \file
 * \brief MiniPort-View + MiniPort-595 + miniPort-KEY 示例程序(带595)，仅供参考
 *
 * - 操作步骤
 *   1. 先将 MiniPort-595 板子直接与底板(AMKS16Z-Core)标号为J4的 MiniPort 相连接；
 *   2. 将 MiniPort-View 板子与 MiniPort-595 接口相连接；
 *   3. 将 miniPort-KEY 与 MiniPort-View 连接；
 *   4. 在 am_prj_config.h 里面将 AM_CFG_KEY_GPIO_ENABLE 和 AM_CFG_KEY_ENABLE
 *      定义为1。
 *
 * - 实验现象：
 *   1. 按下一个按键，两位数码管显示按键编号的二进制；
 *   2. 偶数编号时，数码管闪烁，奇数编号时，数码管不闪烁。
 *
 * \par 源代码
 * \snippet demo_miniport_hc595_digitron_key.c src_miniport_hc595_digitron_key
 *
 * \internal
 * \par Modification history
 * - 1.00 15-07-21  tee, first implementation.
 * \endinternal
 */

