/**
 * \file
 * \brief 时钟频率输出例程，通过 HW 层接口实现
 *
 * - 实验现象：
 *   1. 串口打印 "The CLKOUT  rate is  xxx Hz\r\n"，xxx 为当前频率；
 *   2. PIO0_27 引脚（默认配置 CLKOUT 引脚输出）输出当前时钟频率 2.5MHz；
 *   3. LED0 以 0.2s 的时间间隔闪烁。
 *
 * \note
 *    1. LED0 需要短接 J9 跳线帽，才能被 PIO0_20 控制；
 *    2. 如需观察串口打印的调试信息，需要将 PIO0_0 引脚连接 PC 串口的 TXD，
 *       PIO0_4 引脚连接 PC 串口的 RXD。
 *
 * \par 源代码
 * \snippet demo_am824_hw_clkout.c src_am824_hw_clkout
 *
 * \internal
 * \par Modification history
 * - 1.00 15-07-21  win, first implementation
 * \endinternal
 */

/**
 * \addtogroup demo_if_am824_hw_clkout
 * \copydoc demo_am824_hw_clkout.c
 */

/** [src_am824_hw_clkout] */
#include "ametal.h"
#include "am_vdebug.h"
#include "am_board.h"
#include "demo_nxp_entries.h"

/**
 * \brief 例程入口
 */
void demo_am824_core_hw_clkout_entry (void)
{
    am_kprintf("demo am824 hw clkout!\r\n");
    /* 配置引脚为时钟输出 */
    am_gpio_pin_cfg(PIO0_27, PIO_FUNC_CLKOUT);

    demo_lpc824_hw_clkout_entry();
}
/** [src_am824_hw_clkout] */

